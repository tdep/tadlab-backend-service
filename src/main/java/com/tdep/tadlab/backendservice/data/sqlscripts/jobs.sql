-- Create a job
CREATE TABLE JOB
    (ID NUMERIC,
    NAME VARCHAR(255),
    START_DATE DATE,
    END_DATE DATE,
    CONSTRAINT JOB_PK PRIMARY KEY(ID));

CREATE TABLE ROLE
    (ID NUMERIC,
    TITLE VARCHAR(255),
    DESCRIPTION VARCHAR(1024),
    JOB NUMERIC,
    CONSTRAINT ROLE_PK PRIMARY KEY (ID));

ALTER TABLE JOB
    ADD CONSTRAINT UN_JOB_NAME
    UNIQUE (NAME);

ALTER TABLE ROLE
    ADD CONSTRAINT FK_ROLE_JOB
    FOREIGN KEY (JOB)
    REFERENCES JOB (ID);